/* Declarando Variáveis */

/*
    Utilizar o padrão camelCase. Ex: myAge
*/

/*
    let
    - Permite sobrescrever o valor da variável
*/

let spaceship = "FE Supernova"
// FE Supernova
console.log(spaceship)


/*
    const
    - Não permite sobrescrever o valor da variável
*/

const squad = "Estelar"
// Estelar
console.log(squad)


/*
    var
    - Permite sobrescrever o valor da variável
*/

var velocity = 80
// 80
console.log(velocity)


let noValue
// undefined
console.log(noValue)