/* Operações tipo Number */

// Soma
console.log(1 + 4);

// Subtração
console.log(4.5 - 0.3);

// Multiplicação
console.log(4 * 2);

// Divisão
console.log(4 / 2);

// Resto da Divisão - Módulo
console.log(4 % 2);


/* Operações tipo String */

var texto = "Planeta Terra";

// Concatenação
console.log("Planeta" + "Terra");

// Extrair caractere
console.log(texto[2]);

// Tamanho da String
console.log(texto.length);

